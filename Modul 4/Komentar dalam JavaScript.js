// Program menghitung luas persegi panjang

// Deklarasi variabel
let panjang = 10; // dalam satuan cm
let lebar = 5; // dalam satuan cm

/*
Rumus luas persegi panjang:
Luas = panjang × lebar
*/
let luas = panjang * lebar;

// Hasil perhitungan
console.log("Luas persegi panjang: " + luas + " cm²");
